const LEGACY_REGION_LABELS: Record<string, string> = {
  bnei_brak: "בני ברק",
  jerusalem: "ירושלים",
  elad: "אלעד",
  modiin_ilit: "מודיעין עילית",
  beitar_ilit: "ביתר עילית",
  other: "אחר",
};

/** תווית להצגה של עיר/אזור. ממיר slug ישן לעברית; טקסט חופשי מוחזר כמו שהוא. */
export function regionLabel(region: string | null | undefined): string {
  if (!region) return "";
  return LEGACY_REGION_LABELS[region] ?? region;
}

/** ממזג ערי ברירת-מחדל עם ערים מהשרת — ייחודי וממוין בעברית. */
export function buildCityOptions(fromServer: string[] = []): string[] {
  const set = new Set<string>(DEFAULT_CITIES);
  for (const c of fromServer) {
    const v = c?.trim();
    if (v) set.add(regionLabel(v));
  }
  return Array.from(set).sort((a, b) => a.localeCompare(b, "he"));
}

